// Artemis I mission phases by day
export function getMissionPhaseA1(day: number): string {
  if (day < 0) return 'Pre-Launch'
  if (day < 0.08) return 'Launch & Ascent'
  if (day < 0.18) return 'Trans-Lunar Injection'
  if (day < 5.2) return 'Outbound Coast'
  if (day < 5.5) return 'Lunar Flyby'
  if (day < 9.6) return 'DRO Insertion'
  if (day < 15.6) return 'Distant Retrograde Orbit'
  if (day < 16.0) return 'DRO Departure'
  if (day < 19.5) return 'Return Coast'
  if (day < 25.0) return 'Return Coast'
  return 'Re-entry & Splashdown'
}
